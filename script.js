
var btnadd = document.querySelector(".add-btn-1");

btnadd.addEventListener("click", function() {
    var li = document.createElement("li");
    var list = document.querySelector(".buy-list-1");
    var text = document.querySelector(".add-value-1").value;
    li.innerHTML = text;
    list.appendChild(li);
}, false)


